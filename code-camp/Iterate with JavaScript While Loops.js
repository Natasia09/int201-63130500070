// มาลองทำให้ while loop ทำงาน โดยการ push ค่าเข้าไปยัง array

// จงเพิ่มตัวเลข 5 ถึง 0 (รวม 5 และ 0) ตามลำดับจากมากไปน้อยใน myArray โดยใช้ลูป while
// Setup
var myArray = [];
var i = 0 ;
while (i < 5) {
  myArray.push(i);
  i++;
}

